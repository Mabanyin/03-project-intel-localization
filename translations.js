/**
 * Translate the page to the specified language
 * @param {string} lang - Language code (e.g., 'ar', 'en', 'he')
 */
function translatePage(lang) {
  // Get the translation dictionary for the selected language
  // If the language is not found, default to English
  const languageTranslations = translations[lang] || translations['en'];
  
  // Find all elements with data-i18n attribute
  const elements = document.querySelectorAll('[data-i18n]');
  
  // Loop through each element and update its text or placeholder
  elements.forEach(function(element) {
    // Get the translation key from the data-i18n attribute
    const key = element.getAttribute('data-i18n');
    
    // Get the translated text for this key
    const translatedText = languageTranslations[key];
    
    // If a translation exists, update the element
    if (translatedText) {
      // Check if this is an input element with a placeholder
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = translatedText;
      } else {
        // For other elements, update the text content
        element.textContent = translatedText;
      }
    }
  });
  
  // Log for debugging
  console.log('Page translated to:', lang);
}

// Make the translatePage function available globally
window.translatePage = translatePage;
